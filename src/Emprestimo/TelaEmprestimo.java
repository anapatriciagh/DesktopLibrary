    /*Impreime em um JTextArea os detalhes do exemplar a ser emprestado*/
    private void DetalhesExemplar(){
        Exemplar exemplar = new Exemplar();
        exemplar.setCodigoExemplar(exemplar1.getText());
        ExemplarDAO exemplarDAO = new ExemplarDAO();
        Detalhes detalhes = new Detalhes();
        try {
            detalhes = exemplarDAO.DetalhesExemplar(exemplar);
        } catch (SQLException ex) {
            Logger.getLogger(TelaEmprestimo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        jTextAreaDetalhes.setText(detalhes.toString());
    }
    /*Chama o método que seleciona um exemplar para verificar qual seu status*/
    private void VerificarStatus(){
        ExemplarDAO exemplarDAO = new ExemplarDAO();
        Exemplar exemplar = new Exemplar();
        exemplar.setCodigoExemplar(exemplar1.getText());
        try {
            exemplar = exemplarDAO.selecionarStatusExemplar(exemplar.getCodigoExemplar());
        } catch (SQLException ex) {
            Logger.getLogger(TelaEmprestimo.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(!"emprestado".equals(exemplar.getStatus())){
            confirmarEmprestimo();
        }else {
            JOptionPane.showMessageDialog(null, "Exemplar emprestado!");
        }
    }
