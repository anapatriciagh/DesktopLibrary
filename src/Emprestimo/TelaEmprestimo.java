   /*Confirma um emprestimo, chamando os métodos necessários e o método que o direciona para ser salvo no 
    banco de dados da aplicacao*/
    public void confirmarEmprestimo(){
        //A variáveis globais do tipo JtextField recebem o usuario e senha informados
        usuarioEmprestimo1.setText(TelaUsuarioEmprestimo.nomeUsuario.getText());
        senhaUsuarioEmprestimo.setText(TelaUsuarioEmprestimo.senha.getText());
        TelaUsuarioEmprestimo telaUsuarioEmprestimo1 = new TelaUsuarioEmprestimo();
        Usuario usuario = new Usuario();
        //O usuario recebe os dados informados
        usuario.setNomeUsuario(usuarioEmprestimo1.getText());
        usuario.setSenha(senhaUsuarioEmprestimo.getText());
        System.out.println(usuario);
        Usuario usuario1;
        //O usario é direcionado para autenticação
        usuario1 = telaUsuarioEmprestimo1.autenticar(usuario);
        Usuario usuario2 = new Usuario();
        
        try {
            //Os dados do usuario são buscados no banco de dados
            usuario2 = usuario.selecionarUsuario(usuario1);
            
        } catch (SQLException ex) {
            Logger.getLogger(TelaEmprestimo.class.getName()).log(Level.SEVERE, null, ex);
        }
        //A variáveis globais do tipo JtextField recebem o usuario e senha do operador informados
        operadorEmprestimo1.setText(Login.nomeOperador.getText());
        senhaoperadorEmprestimo.setText(Login.passwordOperador.getText());
        Operador operador = new Operador();
        //O operador recebe os dados informados
        operador.setNomeOperador(operadorEmprestimo1.getText());
        operador.setSenha(senhaoperadorEmprestimo.getText());
        Operador operador1 = new Operador();
        try {
            //Os dados do operador são buscados no banco de dados
            operador1 = operador.buscarOperador(operador);
        } catch (SQLException ex) {
            Logger.getLogger(TelaEmprestimo.class.getName()).log(Level.SEVERE, null, ex);
        }
        //o emprestimo recebe os dados do usurio e operador buscados e o exemplar informado
        Emprestimo emprestimo = new Emprestimo();
        Exemplar exemplar = new Exemplar();
        exemplar.setCodigoExemplar(exemplar1.getText());
        emprestimo.setExemplar(exemplar);
        emprestimo.setOperador(operador1);
        emprestimo.setUsuario(usuario2);
        //O emprestmo recebe a data do sistema 
        emprestimo.setDataEmprestimo(Calendar.getInstance().getTime());
        //operação para se obter a data prevista para devolucao
        //a data prevista recebe a data do emprestimo e soma à quantidade de dias de emprestimo do usuario
        int diasEmprestimo = 0;
        try {
            //Busca a quantidade de dias do emprestimo de acordo com o tipo de usuario
            diasEmprestimo = emprestimo.returnQuantidadeDiasEmprestimo(usuario1.getNomeUsuario(), usuario1.getSenha());
        } catch (SQLException ex) {
            Logger.getLogger(TelaEmprestimo.class.getName()).log(Level.SEVERE, null, ex);
        }
        //A variavel recebe a data do emprestimo mais a quantidade de dias do emprestimo
        int date = emprestimo.getDataEmprestimo().getDate() + diasEmprestimo;
        Date dataPrevista;
         
        dataPrevista = Calendar.getInstance().getTime();
        //A variável recebe a data prevista
        dataPrevista.setDate(date);
        emprestimo.setDataPrevistaDevolucao(dataPrevista);
        try {
            //O emprestimo é salvo
            emprestimo.salvar(emprestimo);
        } catch (SQLException | JRException ex) {
            Logger.getLogger(TelaEmprestimo.class.getName()).log(Level.SEVERE, null, ex);
        } 
        System.out.println(emprestimo.getDataEmprestimo());
        System.out.println(emprestimo.getDataPrevistaDevolucao());
  }
